#define MASK10 8380416  //((1 << 10) - 1) << 13
#define MASK13 8191     //仮数部下位13bit

long long unsigned int make_l[1024];

uint32_t finv(uint32_t a_uint32) {
  
  union data_32bit a, result;
  
  a.uint32 = a_uint32;

  if (a.exp == 255 && a.frac != 0) {  // NaN
    result.uint32 = MY_NAN;
  } else if (a.exp == 0 && a.frac != 0) {// 非正規化数
    if (a.sign == 0) {
      result.uint32 = INF;
    } else {
      result.uint32 = NINF;
    }
  } else if (a.uint32 == INF) {
    result.uint32 = ZERO;
  } else if (a.uint32 == NINF) {
    result.uint32 = NZERO;
  } else if (a.uint32 == ZERO) {
    result.uint32 = INF;
  } else if (a.uint32 == NZERO) {
    result.uint32 = NINF;
  } else if (a.frac == 0) {
    int diff;
    result.uint32 = a.uint32;
    if (a.exp >= 127) {
      diff = a.exp - 127;
      result.exp = 127 - diff;
    } else {
      diff = 127 - a.exp;
      result.exp = 127 + diff;
    }
  } else if (a.exp == 254) {
    if (a.sign == 0) {
      result.uint32 = ZERO;
    } else {
      result.uint32 = NZERO;
    }
  } else {
    
    int index;
    long long unsigned int l;
    unsigned int y,d;
    index = (a.frac & MASK10) >> 13;
    l = make_l[index];
    y = (l >> 13) & 0x7fffff;
    d = l & 0x1fff;
    
    result.sign = a.sign;
    result.exp  = 253 - a.exp;
    result.frac = y + ((d * (8192 - (a.frac & MASK13)) + 0x800) >> 12);
  }
  return (result.uint32);
}

